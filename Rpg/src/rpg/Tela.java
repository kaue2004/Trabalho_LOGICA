    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String Verificador = nome.getText();
        
        if (Verificador.equals("")) {
            JOptionPane.showMessageDialog(null, "Escreva Seu Nome");
        } else {

            String arq = "src/arquivo/NomeParticipante.txt";
            File p = new File(arq);

            String text = nome.getText();

            try {
                FileWriter fw = new FileWriter(p);
                BufferedWriter bw = new BufferedWriter(fw);
                bw.write(""+text);
                bw.newLine();
                bw.close();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

           
            
        }
        dispose();
        new Intro().setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

